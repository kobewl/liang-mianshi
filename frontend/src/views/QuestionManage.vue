<script>
import { ref, reactive, onMounted } from 'vue';
import { message } from 'ant-design-vue';
import { useRouter } from 'vue-router';
import { useStore } from 'vuex';
import { getQuestionList, addQuestion, updateQuestion, deleteQuestion as deleteQuestionApi } from '../api';

export default {
  name: 'QuestionManage',
  setup() {
    const router = useRouter();
    const store = useStore();
    const loading = ref(false);
    const modalVisible = ref(false);
    const isEdit = ref(false);
    const formRef = ref();
    
    const questions = ref([]);

    const columns = [
      {
        title: 'ID',
        dataIndex: 'id',
        key: 'id',
      },
      {
        title: '题目',
        dataIndex: 'title',
        key: 'title',
      },
      {
        title: '内容',
        dataIndex: 'content',
        key: 'content',
        ellipsis: true,
      },
      {
        title: '答案',
        dataIndex: 'answer',
        key: 'answer',
        ellipsis: true,
      },
      {
        title: '难度',
        dataIndex: 'difficulty',
        key: 'difficulty',
      },
      {
        title: '标签',
        dataIndex: 'tags',
        key: 'tags',
      },
      {
        title: '创建时间',
        dataIndex: 'createTime',
        key: 'createTime',
      },
      {
        title: '操作',
        key: 'action',
      },
    ];

    const questionForm = reactive({
      id: null,
      title: '',
      content: '',
      answer: '',
      difficulty: '简单',
      tags: '',
      questionBankId: null
    });

    const rules = {
      title: [
        { required: true, message: '请输入题目标题', trigger: 'blur' }
      ],
      content: [
        { required: true, message: '请输入题目内容', trigger: 'blur' }
      ],
      answer: [
        { required: true, message: '请输入答案', trigger: 'blur' }
      ],
      difficulty: [
        { required: true, message: '请选择难度', trigger: 'change' }
      ]
    };

    // 获取题目列表
    const fetchQuestions = async () => {
      loading.value = true;
      try {
        const response = await getQuestionList({
          current: 1,
          pageSize: 100
        });
        
        if (response.code === 200) {
          questions.value = response.data.records || [];
        } else {
          message.error(response.message || '获取题目列表失败');
        }
      } catch (error) {
        console.error('获取题目列表错误:', error);
        message.error('获取题目列表失败，请检查网络连接');
      } finally {
        loading.value = false;
      }
    };

    const showAddModal = () => {
      isEdit.value = false;
      modalVisible.value = true;
      resetForm();
    };

    const editQuestion = (record) => {
      isEdit.value = true;
      modalVisible.value = true;
      Object.assign(questionForm, record);
    };

    const handleDeleteQuestion = async (id) => {
      try {
        const response = await deleteQuestionApi(id);
        
        if (response.code === 200) {
          message.success('删除成功');
          // 重新获取题目列表
          await fetchQuestions();
        } else {
          message.error(response.message || '删除失败');
        }
      } catch (error) {
        console.error('删除题目错误:', error);
        message.error('删除失败，请检查网络连接');
      }
    };

    const handleSubmit = async () => {
      try {
        await formRef.value.validate();
        
        let response;
        if (isEdit.value) {
          // 编辑题目
          response = await updateQuestion(questionForm);
        } else {
          // 添加题目
          response = await addQuestion(questionForm);
        }
        
        if (response.code === 200) {
          message.success(isEdit.value ? '编辑成功' : '添加成功');
          modalVisible.value = false;
          // 重新获取题目列表
          await fetchQuestions();
        } else {
          message.error(response.message || (isEdit.value ? '编辑失败' : '添加失败'));
        }
      } catch (error) {
        console.log('表单验证失败:', error);
      }
    };

    const handleCancel = () => {
      modalVisible.value = false;
      resetForm();
    };

    const resetForm = () => {
      Object.assign(questionForm, {
        id: null,
        title: '',
        content: '',
        answer: '',
        difficulty: '简单',
        tags: '',
        questionBankId: null
      });
    };

    const handleLogout = () => {
      // 使用Vuex store管理登出状态
      store.dispatch('logout');
      message.success('已退出登录');
      router.push('/login');
    };

    onMounted(() => {
      fetchQuestions();
    });

    return {
      loading,
      modalVisible,
      isEdit,
      questions,
      columns,
      questionForm,
      rules,
      formRef,
      showAddModal,
      editQuestion,
      handleDeleteQuestion,
      handleSubmit,
      handleCancel,
      handleLogout
    };
  }
}
</script>

